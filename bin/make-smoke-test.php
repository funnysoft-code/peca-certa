#!/usr/bin/env php
<?php

declare(strict_types=1);

/**
 * make-smoke-test — generate tests/Browser/SmokeTest.php from route:list
 *
 * Reads `php artisan route:list --json`, groups GET routes by auth middleware
 * presence, then emits a Pest browser test file with two describe() blocks:
 *   - Guest routes  → visited without authentication
 *   - Auth routes   → visited as a logged-in User factory instance
 *
 * Usage:
 *   php bin/make-smoke-test.php
 *   php bin/make-smoke-test.php --output tests/Browser/SmokeTest.php
 *   php bin/make-smoke-test.php --dry-run   (print to stdout, do not write)
 */
$options = getopt('', ['output:', 'dry-run']);
$outputPath = $options['output'] ?? 'tests/Browser/SmokeTest.php';
$dryRun = isset($options['dry-run']);

// -------------------------------------------------------------------------- //
// 1. Fetch route list
// -------------------------------------------------------------------------- //

$raw = shell_exec('php artisan route:list --json 2>/dev/null');
if ($raw === null || $raw === '') {
    fwrite(STDERR, "Error: could not run `php artisan route:list --json`.\n");
    fwrite(STDERR, "Make sure you run this script from the project root.\n");
    exit(1);
}

/** @var list<array{method: string, uri: string, name: ?string, middleware: string|list<string>}> $routes */
$routes = json_decode($raw, true, 512, JSON_THROW_ON_ERROR);

// -------------------------------------------------------------------------- //
// 2. Filter to GET routes, exclude vendor/debug/horizon/telescope/health
// -------------------------------------------------------------------------- //

$skipPrefixes = [
    '_ignition', '_debugbar', 'horizon', 'telescope', 'api/', 'sanctum',
    'livewire', 'vapor', '_vapor', 'pulse',
];

$isSkipped = static function (string $uri) use ($skipPrefixes): bool {
    foreach ($skipPrefixes as $prefix) {
        if (str_starts_with($uri, $prefix)) {
            return true;
        }
    }

    return false;
};

$getRoutes = array_filter($routes, static function (array $route) use ($isSkipped): bool {
    $method = mb_strtoupper($route['method'] ?? '');
    $uri = mb_ltrim($route['uri'] ?? '', '/');

    return str_contains($method, 'GET') && ! $isSkipped($uri);
});

// -------------------------------------------------------------------------- //
// 3. Group by auth middleware
// -------------------------------------------------------------------------- //

$authRoutes = [];
$guestRoutes = [];

foreach ($getRoutes as $route) {
    $middleware = $route['middleware'] ?? [];
    if (is_string($middleware)) {
        $middleware = array_filter(array_map('trim', explode(',', $middleware)));
    }

    $uri = '/'.mb_ltrim($route['uri'], '/');
    $name = $route['name'] ?? null;
    $label = $name ?? $uri;

    $requiresAuth = false;
    foreach ($middleware as $mw) {
        if (str_starts_with($mw, 'auth')) {
            $requiresAuth = true;
            break;
        }
    }

    if ($requiresAuth) {
        $authRoutes[] = ['uri' => $uri, 'label' => $label];
    } else {
        $guestRoutes[] = ['uri' => $uri, 'label' => $label];
    }
}

// -------------------------------------------------------------------------- //
// 4. Render test cases
// -------------------------------------------------------------------------- //

$renderCase = static function (array $route): string {
    $label = addslashes($route['label']);
    $uri = addslashes($route['uri']);
    // Skip routes with required dynamic segments — they need real IDs
    if (preg_match('/\{[^?}]+\}/', $route['uri'])) {
        return sprintf(
            "    // Skipped: '%s' has required route parameters (%s)\n",
            $label,
            $route['uri'],
        );
    }

    return sprintf(
        "    it('loads %s without JS errors', function () {\n".
        "        \$this->visit('%s')\n".
        "             ->waitForPageToLoad()\n".
        "             ->assertNoJsErrors();\n".
        "    });\n\n",
        $label,
        $uri,
    );
};

$guestBlock = '';
foreach ($guestRoutes as $route) {
    $guestBlock .= $renderCase($route);
}

$authBlock = '';
foreach ($authRoutes as $route) {
    $authBlock .= $renderCase($route);
}

// -------------------------------------------------------------------------- //
// 5. Emit the test file
// -------------------------------------------------------------------------- //

$timestamp = date('Y-m-d');
$output = <<<PHP
<?php

declare(strict_types=1);

/**
 * Smoke test — auto-generated by bin/make-smoke-test.php on {$timestamp}
 *
 * Visits every GET route without JS errors. Routes with required dynamic
 * segments are commented out — wire them up manually with real model IDs
 * (or use route model binding via factory).
 *
 * Run:
 *   php artisan test --compact tests/Browser/SmokeTest.php
 */

use App\\Models\\User;

// -------------------------------------------------------------------------- //
// Guest routes (no auth middleware)
// -------------------------------------------------------------------------- //

describe('Guest routes', function () {
{$guestBlock}});

// -------------------------------------------------------------------------- //
// Auth routes (requires authentication)
// -------------------------------------------------------------------------- //

describe('Auth routes', function () {
    beforeEach(function () {
        \$this->actingAs(User::factory()->create());
    });

{$authBlock}});
PHP;

if ($dryRun) {
    echo $output."\n";
    exit(0);
}

// Ensure directory exists
$dir = dirname($outputPath);
if (! is_dir($dir) && ! mkdir($dir, 0755, true) && ! is_dir($dir)) {
    fwrite(STDERR, "Error: could not create directory '{$dir}'.\n");
    exit(1);
}

file_put_contents($outputPath, $output."\n");
echo "Smoke test written to: {$outputPath}\n";
echo sprintf(
    "  Guest routes: %d  |  Auth routes: %d\n",
    count($guestRoutes),
    count($authRoutes),
);
